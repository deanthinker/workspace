
public class PriceData{
	public String pcode = "";
	public String crop = "";
	public String pcname = "";
	public float up = 0;
	public float sales = 0;
	public float weight = 0;
	
	
	public PriceData(String pcode, String crop, String pcname, float up, float sales, float weight){
		this.pcode = pcode;
		this.crop = crop;
		this.pcname = pcname;
		this.up = up;
		this.sales = sales;
		this.weight = weight;		
	}
	
}
